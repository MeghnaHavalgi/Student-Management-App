    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
    int userIdToUpdate = Integer.parseInt(txtnuid.getText());
    String newUsername = txtUsername.getText();
    String newPassword = txtPassword.getText();

    try {
        User loggedUser = ulist.getUserById(userIdToUpdate);
        if (loggedUser != null) {
            // Check if the new password matches the current password
            if (newPassword.equals(loggedUser.getLastUsedPassword())) {
                JOptionPane.showMessageDialog(this, "The new password matches the old password. Please try again with a new Password");
            } else {
                // Update the username and password
                ulist.updateUsernameAndPassword(userIdToUpdate, newUsername, newPassword);

               
                // Get the new first name and last name from text fields
                String newFirstName = txtFirstName.getText();
                String newLastName = txtLastName.getText();

                // Call the updateUser method to update the first name and last name
                ulist.updateUser(userIdToUpdate, newFirstName, newLastName);
                
                JOptionPane.showMessageDialog(this, "User information updated successfully.");
                 // Refresh the fields with the updated user data
                populateFields(userIdToUpdate);
            }
        } else {
            JOptionPane.showMessageDialog(this, "User not found.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid user ID.");
    }
    
    
    
    }//GEN-LAST:event_btnUpdateActionPerformed

