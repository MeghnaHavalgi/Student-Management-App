    private void populateFields(int userId) {
    try {

            User loggedUser = ulist.getUserById(userId); 

            if (loggedUser != null) {
                // Populate the fields with user details
                txtFirstName.setText(loggedUser.getFname());
                txtLastName.setText(loggedUser.getLname());
                txtUsername.setText(loggedUser.getUsername());
                txtnuid.setText(String.valueOf(userId));
                txtPassword.setText(loggedUser.getLastUsedPassword());
                // ...
            } else {
                JOptionPane.showMessageDialog(this, "User not found.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please provide a valid user ID.");
        }
}
