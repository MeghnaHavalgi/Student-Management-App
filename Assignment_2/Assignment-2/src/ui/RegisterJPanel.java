    public User authenticateUser(String username, String password) {
         if (ulist == null) {
            throw new IllegalStateException("UserList is not properly initialized.");
        }
        ArrayList<User> users = ulist.getAllUsers();

        for (User user : users) {
            if (user.getUsername().equals(username) && user.getHshpassword().equals(user.hashPassword(password))) {
                return user; // for Authentication successful
            }
        }
        return null; // for Authentication failed
    }
    
