    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
if (isFieldEmpty(txtFname.getText()) || isFieldEmpty(txtLname.getText()) || isFieldEmpty(txtUsername.getText()) || isFieldEmpty(txtPassword.getText())) {
            JOptionPane.showMessageDialog(this, "All fields are required. Please fill in all the fields.");
            return;
        }
        
        User user = ulist.addNewUser();
        
        user.setFname(txtFname.getText());
        user.setLname(txtLname.getText());
        user.setUsername(txtUsername.getText());
        user.setHshpassword(txtPassword.getText());
        user.setId();
        user.setStatus(Boolean.TRUE);
       
        
        if (txtFname.getText().trim().isEmpty() && txtLname.getText().trim().isEmpty() && txtUsername.getText().trim().isEmpty() && txtPassword.getText().trim().isEmpty() )
{
lblInval.setText("Fields are empty");
}       
    else 
{
        
        lblInval.setText("");
        
        JOptionPane.showMessageDialog(this, "New User Added");
        
}
        
        txtFname.setText("");
        txtLname.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
        

    }//GEN-LAST:event_btnSubmitActionPerformed
    
